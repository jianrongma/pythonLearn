# 1.算术运算符:+ - * / // % **
print("1 + 1 =",1 + 1)
print("9 / 3 =",9 / 3)
print("9 // 2 =",9 // 2)
print("9 % 2 =",9 % 2)
print("2 ** 2 =",2 ** 2)

# 2.赋值运算符
num = 1 + 2 * 3

# 3.复合赋值运算符
# +=
num = 1
num += 1 # num = num + 1
print("num += 1: ", num)
# -=
num -= 1
print("num -= 1: ", num)
# *=
num *= 4
print("num *= 4: ", num)
# /=
num /= 2
print("num /= 2: ", num)
# //=
num = 9
num //= 2
print("num //= 2:", num)
# %=
num = 3
num %= 2
print("num %= 2: ", num)
# **=
num **= 2
print("num **=2: ", num)
